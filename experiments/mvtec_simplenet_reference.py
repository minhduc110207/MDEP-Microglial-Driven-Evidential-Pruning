"""
SimpleNet-style reference baseline for MVTec AD image-level anomaly detection.

This runner follows the core SimpleNet recipe in a compact in-repo form:
pretrained patch features, a shallow feature adapter, synthetic anomaly features
generated by Gaussian perturbation, and a lightweight discriminator. It is a
paper-facing reference baseline, not the official authors' code.
"""

