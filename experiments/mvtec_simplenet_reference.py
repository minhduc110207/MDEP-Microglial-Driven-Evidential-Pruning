def output_root() -> Path:
    root = Path("/kaggle/working") if Path("/kaggle/working").exists() else REPO_ROOT
    return root / "paper_experiment_outputs" / "mvtec_simplenet"


def seed_everything(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


class PatchFeatureExtractor(nn.Module):
    def __init__(self, pretrained: bool = True):
        super().__init__()
        weights = models.ResNet18_Weights.DEFAULT if pretrained else None
        try:
            backbone = models.resnet18(weights=weights)
        except Exception as exc:
            print(f"[WARN] Could not load pretrained ResNet-18 weights ({exc}); using random init.")
            backbone = models.resnet18(weights=None)
        self.stem = nn.Sequential(backbone.conv1, backbone.bn1, backbone.relu, backbone.maxpool)
        self.layer1 = backbone.layer1
        self.layer2 = backbone.layer2
        self.layer3 = backbone.layer3
        for param in self.parameters():
            param.requires_grad = False

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = self.stem(x)
        x = self.layer1(x)
        x = self.layer2(x)
        x = self.layer3(x)
        patches = x.flatten(2).transpose(1, 2)
        return torch.nn.functional.normalize(patches, dim=-1)


class SimpleNetHead(nn.Module):
    def __init__(self, feature_dim: int, hidden_dim: int = 256):
        super().__init__()
        self.adapter = nn.Sequential(
            nn.Linear(feature_dim, feature_dim),
            nn.LayerNorm(feature_dim),
            nn.ReLU(inplace=True),
        )
        self.discriminator = nn.Sequential(
            nn.Linear(feature_dim, hidden_dim),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(hidden_dim, 1),
        )

    def forward(self, patches: torch.Tensor) -> torch.Tensor:
        adapted = self.adapter(patches)
        return self.discriminator(adapted).squeeze(-1)

    def adapted_features(self, patches: torch.Tensor) -> torch.Tensor:
        return self.adapter(patches)


def sample_patches(patches: torch.Tensor, max_patches: int) -> torch.Tensor:
    patches = patches.reshape(-1, patches.shape[-1])
    if patches.shape[0] <= max_patches:
        return patches
    idx = torch.randperm(patches.shape[0], device=patches.device)[:max_patches]
    return patches[idx]


def train_simplenet(
    extractor: PatchFeatureExtractor,
    head: SimpleNetHead,
    loader,
    device: torch.device,
    epochs: int,
    lr: float,
    noise_std: float,
    max_patches_per_batch: int,
) -> list[dict[str, float]]:
    extractor.eval()
    head.train()
    optimizer = torch.optim.AdamW(head.parameters(), lr=lr, weight_decay=1e-5)
    criterion = nn.BCEWithLogitsLoss()
    history = []
    for epoch in range(epochs):
        losses = []
        for inputs, _ in loader:
            inputs = inputs.to(device)
            with torch.no_grad():
                patches = extractor(inputs)
            patches = sample_patches(patches, max_patches_per_batch)
            normal_features = head.adapted_features(patches)
            anomaly_features = normal_features.detach() + noise_std * torch.randn_like(normal_features)
            normal_logits = head.discriminator(normal_features).squeeze(-1)
            anomaly_logits = head.discriminator(anomaly_features).squeeze(-1)
            logits = torch.cat([normal_logits, anomaly_logits], dim=0)
            targets = torch.cat([
                torch.zeros_like(normal_logits),
                torch.ones_like(anomaly_logits),
            ], dim=0)
            loss = criterion(logits, targets)
            optimizer.zero_grad(set_to_none=True)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(head.parameters(), max_norm=1.0)
            optimizer.step()
            losses.append(float(loss.detach().cpu()))
        avg_loss = float(np.mean(losses)) if losses else 0.0
        history.append({"epoch": epoch + 1, "loss": avg_loss})
        print(f"[SimpleNet] epoch={epoch + 1:03d}/{epochs:03d} loss={avg_loss:.4f}")
    return history


@torch.no_grad()
def score_images(
    extractor: PatchFeatureExtractor,
    head: SimpleNetHead,
    loader,
    device: torch.device,
) -> tuple[np.ndarray, np.ndarray]:
    extractor.eval()
    head.eval()
    labels = []
    scores = []
    for inputs, targets in loader:
        inputs = inputs.to(device)
        patches = extractor(inputs)
        bsz, num_patches, dim = patches.shape
        logits = head(patches.reshape(-1, dim)).view(bsz, num_patches)
        image_scores = torch.sigmoid(logits).max(dim=1).values
        labels.append(targets.numpy())
        scores.append(image_scores.detach().cpu().numpy())
    return np.concatenate(labels).astype(int), np.concatenate(scores)


def run_one(args: argparse.Namespace, seed: int) -> dict:
    seed_everything(seed)
    category_dir = _find_mvtec_category_dir(args.category)
    if category_dir is None:
        raise FileNotFoundError(
            f"Real MVTec category '{args.category}' not found. Add MVTec AD under /kaggle/input or set MVTEC_ROOT."
        )

    train_good, test_samples = collect_category_samples(category_dir)
    transform = transforms.Compose([
        transforms.Resize((args.image_size, args.image_size)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
    ])
    workers = 0 if os.name == "nt" else 2
    train_loader = DataLoader(
        MVTecImageLevelDataset(train_good, transform=transform),
        batch_size=args.batch_size,
        shuffle=True,
        num_workers=workers,
    )
    test_loader = DataLoader(
        MVTecImageLevelDataset(test_samples, transform=transform),
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=workers,
    )

    device = torch.device("cuda" if torch.cuda.is_available() and not args.cpu else "cpu")
    extractor = PatchFeatureExtractor(pretrained=not args.no_pretrained).to(device)
    with torch.no_grad():
        sample_inputs, _ = next(iter(train_loader))
        feature_dim = extractor(sample_inputs[:1].to(device)).shape[-1]
    head = SimpleNetHead(feature_dim=feature_dim, hidden_dim=args.hidden_dim).to(device)
    history = train_simplenet(
        extractor,
        head,
        train_loader,
        device,
        args.epochs,
        args.lr,
        args.noise_std,
        args.max_patches_per_batch,
    )
    y_true, anomaly_scores = score_images(extractor, head, test_loader, device)
    probs = np.stack([1.0 - anomaly_scores, anomaly_scores], axis=1)
    metrics = binary_image_anomaly_metrics(y_true, probs, prefix="simplenet_")
    metrics.update({
        "train_good_images": float(len(train_good)),
        "test_images": float(len(test_samples)),
    })

    run_dir = output_root() / args.category / f"seed_{seed}"
    run_dir.mkdir(parents=True, exist_ok=True)
    result = {
        "benchmark": "mvtec_simplenet",
        "run_name": args.category,
        "experiment": {
            "name": "simplenet_style_resnet18",
            "family": "anomaly_detection_reference",
            "description": "SimpleNet-style reference with pretrained patch features, adapter, synthetic feature anomalies, and discriminator.",
        },
        "seed": seed,
        "category_dir": category_dir,
        "history": history,
        "metrics": metrics,
    }
    (run_dir / "metrics.json").write_text(json.dumps(json_safe(result), indent=2), encoding="utf-8")
    print(json.dumps(json_safe(result), indent=2))
    return result


def main() -> int:
    parser = argparse.ArgumentParser(description="Run SimpleNet-style MVTec AD reference baseline.")
    parser.add_argument("--category", type=str, default="hazelnut")
    parser.add_argument("--batch_size", type=int, default=16)
    parser.add_argument("--image_size", type=int, default=224)
    parser.add_argument("--epochs", type=int, default=10)
    parser.add_argument("--lr", type=float, default=1e-3)
    parser.add_argument("--noise_std", type=float, default=0.015)
    parser.add_argument("--hidden_dim", type=int, default=256)
    parser.add_argument("--max_patches_per_batch", type=int, default=4096)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--seeds", type=int, nargs="+")
    parser.add_argument("--no_pretrained", action="store_true")
    parser.add_argument("--cpu", action="store_true")
    args = parser.parse_args()

    seeds = args.seeds if args.seeds else [args.seed]
    for seed in seeds:
        run_one(args, seed)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
